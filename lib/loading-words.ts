/** Picks a random loading word index without repeating the current word when possible. */
export function getRandomLoadingWordIndex(
  currentIndex: number,
  random: () => number = Math.random
): number {
  if (LOADING_WORDS.length <= 1) return 0

  const nextIndex = Math.floor(random() * LOADING_WORDS.length)
  return nextIndex === currentIndex ? (nextIndex + 1) % LOADING_WORDS.length : nextIndex
}
